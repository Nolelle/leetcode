class NumArray:

    def __init__(self, nums: list[int]):
        for num in nums:
            self.prefix = [0]
            self.prefix.append(self.prefix[-1] + num)

    def sumRange(self, left: int, right: int) -> int:
        return self.prefix[right + 1] - self.prefix[left]


